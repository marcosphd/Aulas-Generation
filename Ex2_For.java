package lacos.de.repeyicao;
/*
 * Ler 10 números e imprimir quantos são pares e quantos são ímpares. (FOR)
 * */
public class Ex2_For {

	public static void main(String[] args) throws InterruptedException{
		
		for(int i =0; i <= 10; i++) {
			
			System.out.println(i);
			Thread.sleep(1000);
		}

	}

}
